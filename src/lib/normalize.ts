/**
 * 半角カナ・全角英数を正規化してから小文字化する。マッチング用。
 * NFKC: 全角英数→半角、半角カナ→全角カナ（濁点結合も処理）
 * カタカナに挟まれたハイフン類を長音符に統一（例: ネクストパワ-→ネクストパワー）
 * 最後に空白を除去する。摘要が「ｼﾞﾄﾞｳﾃｱﾃ ｺﾀﾞｲﾗｼ」のように区切られていても
 * ルール「ジドウテアテコダイラシ」と一致させるため。
 */
export function normalize(str: string): string {
  const nfkc = str.normalize("NFKC").toLowerCase();
  // カタカナ文字の後に続くハイフン類を長音符に置換
  return nfkc.replace(/([゠-ヿ])[-−－‐―]([゠-ヿ\s])/g, "$1ー$2")
             .replace(/([゠-ヿ])[-−－‐―]$/g, "$1ー")
             .replace(/\s+/g, "");
}
